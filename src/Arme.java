public class Arme {
    String nom;
    int stat;
    int effet;
    String description;
    String classeArme;
    public Arme (String nom, int stat, int effet, String description, String classeArme) {
        this.nom = nom;
        this.stat = stat;
        this.effet = effet;
        this.description = description;
        this.classeArme = classeArme;
    }
}
